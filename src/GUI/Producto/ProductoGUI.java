    public int jcheckSelecionar() {
        try {
            String nombre = jcomboxUnidadMedida.getSelectedItem().toString();
            switch (nombre) {
                case "UNIDADES":
                    jradUND.setSelected(true);
                    op = 1;
                    break;
                case "KILOGRAMOS":
                    jradKG.setSelected(true);
                    op = 2;
                    break;
                case "ROLLO":
                    jradRollo.setSelected(true);
                    op = 3;
                    break;
                case "GALON":
                    jradGL.setSelected(true);
                    op = 4;
                    break;
            }
        } catch (Exception e) {
        }
        return op;
    }
