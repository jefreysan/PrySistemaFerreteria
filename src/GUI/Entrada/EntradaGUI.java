    private void limpiarControles() {
        limpiarJTable();
        JTextField[] objTextFields = { jtxtnroEntrada,
            jtxtcodigoProveedor, jtxtnombreProveedor, jtxtcodigoEncargado,
            jtxtnombreEncargado, jtxtcodigoProducto, jtxtDescrProducto,
            jtxtprecioVenta, jtxtcantidad, jtxtTotal};
        for (JTextField objTextField : objTextFields) {
            objTextField.setText(null);
        }
        objButtonGroup.clearSelection();
        jDateFechaRegistro.setCalendar(null);
    }

    private void limpiarJTable() {
        while (objDtm.getRowCount() > 0) {
            objDtm.removeRow(0);
        }
    }

    private void calcularTotales() {
        subTotal = 0;
        total = 0;
        int filas = objDtm.getRowCount();
        int cont = 0;
        while (cont < filas) {
            total = total + Double.parseDouble(objDtm.getValueAt(cont, 4).toString());
            cont++;
        }
        jtxtTotal.setText(String.format("%.2f", total));
    }

    private void limpiarProducto() {
        JTextField[] objTextFields = {jtxtcodigoProducto, jtxtDescrProducto, jtxtprecioVenta, jtxtcantidad};
        for (JTextField objTextField : objTextFields) {
            objTextField.setText(null);
        }
        jcomboxUnidadMedida.removeAllItems();
        jcomboxPresentacion.removeAllItems();
        cantidad = 0;
        precio = 0;
        importe = 0;
    }

    private void llenarComboUDM() {
        try {
            jcomboxPresentacion.removeAllItems();
            String xnombre = jcomboxUnidadMedida.getSelectedItem().toString();
            rspresentacion = objPresentacionDAO.buscarPresentacion(xnombre);
            while (rspresentacion.next()) {
                jcomboxPresentacion.addItem(rspresentacion.getString(3));
            }
        } catch (Exception e) {
        }
    }

