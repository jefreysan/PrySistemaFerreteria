    private void jtxtBuscaProveedorKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtxtBuscaProveedorKeyReleased
        try {
            limpiarJTable();
            if (!jtxtBuscaProveedor.getText().isEmpty()) {
                rsProveedor = objProveedorDAO.buscar(jtxtBuscaProveedor.getText().trim());
                while (rsProveedor.next()) {
                    Object registro[] = {rsProveedor.getInt(1), rsProveedor.getString(2), rsProveedor.getString(3),
                        rsProveedor.getString(4), rsProveedor.getString(5), rsProveedor.getString(6)};
                    objDtm.addRow(registro);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }//GEN-LAST:event_jtxtBuscaProveedorKeyReleased

