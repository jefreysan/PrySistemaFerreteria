    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
            /*COMPROBANTE DE VENTA*/
            ComprobanteTO objComprobanteVentaTO = new ComprobanteTO();
            objComprobanteVentaTO.setIdencargado(Integer.parseInt(jtxtcodigoEncargado.getText()));
            objComprobanteVentaTO.setIdcliente(Integer.parseInt(jtxtcodigoCliente.getText()));
            objComprobanteVentaTO.setIdtipopago(xidtipopago);
            objComprobanteVentaTO.setNrocomp(jtxtSerieNumero.getText().trim());
            objComprobanteVentaTO.setSubTotalcomp(subTotal);
            objComprobanteVentaTO.setIgvcomp(igv);
            objComprobanteVentaTO.setTotalcomp(total);
            objComprobanteVentaDAO.insert(objComprobanteVentaTO);
            xidcomprobante = objComprobanteVentaDAO.obternerIDIngreso();
            /*DETALLE COMPROBANTE DE VENTA*/
            int filas = objDtm.getRowCount();
            for (int i = 0; i < filas; i++) {
                DetalleComprobanteVentaTO objDetalleComprobanteVentaTO = new DetalleComprobanteVentaTO();
                objDetalleComprobanteVentaTO.setIdcomprobanteVenta(xidcomprobante);
                objDetalleComprobanteVentaTO.setIdproducto(Integer.parseInt(jtblComprobante.getValueAt(i, 0).toString()));
                objDetalleComprobanteVentaTO.setPcompracomp(Double.parseDouble(jtblComprobante.getValueAt(i, 2).toString()));
                objDetalleComprobanteVentaTO.setCantidadcomp(Integer.parseInt(jtblComprobante.getValueAt(i, 3).toString()));
                objDetalleComprobanteVentaTO.setImportecomp(Double.parseDouble(jtblComprobante.getValueAt(i, 4).toString()));
                objDetalleComprobanteVentaDAO.insert(objDetalleComprobanteVentaTO);
            }
            /*CREDITO*/
            CreditoTO objCreditoTO = new CreditoTO();
            objCreditoTO.setIdcomprobante(xidcomprobante);
            objCreditoTO.setDeudacred(total);
            objCreditoDAO.insert(objCreditoTO);
            habilitarControles(false);
            JOptionPane.showMessageDialog(rootPane, "COMPROBANTE GRABADO");
            limpiarControles();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

