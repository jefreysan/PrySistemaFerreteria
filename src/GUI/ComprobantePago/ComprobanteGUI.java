    private void habilitarControles(boolean b) {
        JTextField[] arrJTexFields = {jtxtcantidad, jtxtTotalPago};
        for (JTextField obJTextField : arrJTexFields) {
            obJTextField.setEditable(b);
        }
        btnGuardar.setEnabled(b);
        btnNuevo.setEnabled(!b);
        btnCancelar.setEnabled(b);
        btnbuscarCliente.setEnabled(b);
        btnbuscarProveedor.setEnabled(b);
        btnBuscarProducto.setEnabled(b);
        btnEliminarProducto.setEnabled(b);
    }

    private void limpiarControles() {
        limpiarJTable();
        JTextField[] objTextFields = {jtxtcodigoEncargado, jtxtnombreEncargado,
            jtxtcodigoCliente, jtxtnombreCliente, jtxtcodigoProducto,
            jtxtnombreProducto, jtxtPrecioProducto, jtxtcantidad,
            jtxtSubTotal, jtxtIGV, jtxtTotal, jtxtTotalPago, jtxtVuelto};
        for (JTextField objTextField : objTextFields) {
            objTextField.setText(null);
        }

    }

    private void limpiarJTable() {
        while (objDtm.getRowCount() > 0) {
            objDtm.removeRow(0);
        }
    }

    private void calcularTotales() {
        subTotal = 0;
        igv = 0;
        total = 0;
        int filas = objDtm.getRowCount();
        int cont = 0;
        while (cont < filas) {
            total = total + Double.parseDouble(objDtm.getValueAt(cont, 4).toString());
            cont++;
        }
        subTotal = total / 1.18;
        igv = total - subTotal;
        jtxtSubTotal.setText(String.format("%.2f", subTotal));
        jtxtIGV.setText(String.format("%.2f", igv));
        jtxtTotal.setText(String.format("%.2f", total));
    }

    private void limpiarProducto() {
        jtxtcodigoProducto.setText(null);
        jtxtnombreProducto.setText(null);
        jtxtPrecioProducto.setText(null);
        jtxtStock.setText(null);
        jtxtcantidad.setText(null);
        cantidad = 0;
        precio = 0;
        importe = 0;
    }
